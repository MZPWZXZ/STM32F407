uint32_t g_time_count = 0;
void TIM1_UP_TIM10_IRQHandler()
{
    if(TIM_GetITStatus(TIM1, TIM_IT_Update) == SET)
    {
        TIM_ClearITPendingBit(TIM1, TIM_IT_Update);
        delay_update();
    }
}

extern uint32_t g_lwip_localtime;
/**
  * @brief  
  * @param  None
  * @retval None
  */
void TIM6_DAC_IRQHandler()
{
    if(TIM_GetITStatus(TIM6, TIM_IT_Update) == SET)
    {
        g_lwip_localtime += 10; 
        TIM_ClearITPendingBit(TIM6, TIM_IT_Update);
    }
}


/**
  * @brief  以太网中断服务函数
  * @param  None
  * @retval None
  */
void ETH_IRQHandler(void)
{
    while(ETH_GetRxPktSize(DMARxDescToGet)!=0) //检测是否收到数据包
    { 
        lwip_pkt_handle();
    }
    ETH_DMAClearITPendingBit(ETH_DMA_IT_R); //清除 DMA 接收中断标志位
    ETH_DMAClearITPendingBit(ETH_DMA_IT_NIS); //清除 DMA 中断标志位
}




